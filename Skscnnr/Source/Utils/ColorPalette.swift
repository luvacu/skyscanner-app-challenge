import UIKit

extension UIColor {

    @nonobjc
    static var charcoalGrey: UIColor {
        return UIColor(red: 52.0 / 255.0, green: 54.0 / 255.0, blue: 61.0 / 255.0, alpha: 1.0)
    }

    @nonobjc
    static var lipstick: UIColor {
        return UIColor(red: 201.0 / 255.0, green: 35.0 / 255.0, blue: 125.0 / 255.0, alpha: 1.0)
    }

    @nonobjc
    static var purpleyGrey: UIColor {
        return UIColor(red: 137.0 / 255.0, green: 130.0 / 255.0, blue: 148.0 / 255.0, alpha: 0.25)
    }

    @nonobjc
    static var turquoiseBlue: UIColor {
        return UIColor(red: 0.0, green: 178.0 / 255.0, blue: 214.0 / 255.0, alpha: 1.0)
    }

}
